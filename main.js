// Creating an object literal

let playStation = {
  'Disk Drive': 'Blu Ray',
  'Hard Drive': 'Lightning Fast SSD',
  Fuel: 'Games'
};

// One of 2 ways of accessing the value of a property. >>Bracket Notation<<
console.log(playStation["Disk Drive"]);

// >>Dot Notation<<
console.log(playStation.Fuel);
